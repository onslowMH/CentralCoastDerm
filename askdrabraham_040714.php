        <h1 class="alt">Physician Q&amp;A with Dr. Abraham</h1>
        <h2 class="alt">Welcome springtime with safe sun exposure –
          
          and get your D3!       </h2>
        <br />
        <h3>by Jennifer Mackenzie, MFA </h3>
       <p>Dr. Daniel Abraham, D.O., specializes in medical and surgical dermatology in the care of all age ranges, newborns to elderly. He has advanced skills in the identification and treatment of difficult/chronic rashes, as well as in the diagnosis and surgical resolution of skin cancer and precancerous lesions.       </p>
       <h3>OMH: Once and for all, are tanning beds “safe”?</h3>
       <p><u>Dr. <strong>Abraham</strong></u>: No! You are exposing your entire body to direct
exposure to UV rays – so it’s worse than being out in the midday
sun. The use of tanning beds is still surprisingly prevalent in this
area, even with the outdoor sun we have available here. I’ve actually
had patients that have had a tanning bed in their homes.
</p>
       <h3>OMH: Have skin cancer rates increased due to use of
tanning beds?
</h3>
       <p><u>Dr. <strong>Abraham</strong></u>: In the last 5-10 years, we’ve seen a significant
increase in basal cell skin cancers among young
women in their 20s, and we have to attribute most of that
to the use of tanning beds.
</p>
       <h3>OMH: What is your advice for safe sun exposure?</h3>
       <p><u>Dr. <strong>Abraham</strong></u>: No. 1, don’t use tanning beds! Avoid the
sun in the midday hours [10 a.m.-2 p.m.]. Always use
an SPF 30 or higher sunscreen, and when possible, wear
protective clothing. I never tell anybody not to go outside
– people should enjoy being outdoors – but be smart
about it.
</p>
       <h3>OMH: What types of skin cancers do you see in this area?
And are most skin cancers treatable?
       </h3>
       <p><u>Dr. <strong>Abraham</strong></u>: We see everything, but mostly basal cell skin
         cancers. [Basal cell cancer, a type of nonmelanoma skin cancer,
         usually appears as a small, fleshy bump or nodule on the head,
         neck, or hands. Occasionally, these nodules appear on the trunk
         of the body, usually as flat growths.] As far as skin cancers being
         treatable, if caught early, even melanoma is treatable. And
         non-melanoma skin cancers like basal cells and squamous skin
         cancers [which may appear on Caucasians as nodules, or as red,
         scaly patches of skin] are typically highly treatable.
</p>
      
       <h2>Vitamin D3 and the sun?</h2>
       <p>According to the Mayo Clinic, and many other sources, the sun adds to the body’s daily production of
vitamin D (actually a hormone). Ten minutes of exposure to the arms and legs prior to applying sunscreen
might prevent deficiency. Two forms are important in humans: vitamin D2, which is made by plants, and
vitamin D3, which is made by human skin when exposed to sunlight. Vitamin D3 assists in the absorption
and utilization of calcium, and promotes bone mineralization, which may prevent or slow the progression
of osteoporosis. Research also strongly suggests that vitamin D3 helps strengthen our immune systems.
The Mayo Clinic states that in addition to protecting against osteoporosis, vitamin D might protect us against high blood pressure, cancer, and other diseases. To find out if you are vitamin D deficient, have your doctor measure your blood levels of 25(OH)D.

</p>




